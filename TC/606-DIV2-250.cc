struct EllysSubstringSorter {
	string getMin(string S, int L) {
		const int N = (int) S.size();

		set<string> ss;

		for (int i = 0; i + L <= N; i++) {
			string cpy = S;

			sort(cpy.begin() + i, cpy.begin() + i + L);

			ss.insert(cpy);
		}

		return *ss.begin();
	}
};

