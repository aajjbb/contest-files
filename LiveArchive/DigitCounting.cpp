int T, N;
int digit[13];

int main(void) {
    T = in();

    int i;

    for ( ; T--; ) {
        N = in();

        for (i = 0; i < 10; i++) digit[i] = 0;

        for (i = 1; i <= N; i++) {
            int x = i;

            for ( ; x > 0; x /= 10) {
                digit[x % 10] += 1;
            }
        }

        for (i = 0; i < 10; i++) {
            printf("%d", digit[i]);
            if (i != 9) printf(" ");
        }
        printf("\n");
    }
    return 0;
}
