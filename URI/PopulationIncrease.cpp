int T;

double PA, PB, G1, G2;

int main(void) {
    cin >> T;

    for ( ; T--; ) {
        cin >> PA >> PB >> G1 >> G2;

        int year = 0;

        for ( ; year <= 100; ) {
            if (PA > PB) {
                break;
            }

            PA = PA + floor(PA * G1 / 100.0);
            PB = PB + floor(PB * G2 / 100.0);

            year += 1;
        }

        if (year > 100) {
            puts("Mais de 1 seculo.");
        } else {
            printf("%d anos.\n", year);
        }
    }

    return 0;
}
