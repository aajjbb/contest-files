template<typename T> T gcd(T a, T b) {
    if(!b) return a;
    return gcd(b, a % b);
}
template<typename T> T lcm(T a, T b) {
    return a * b / gcd(a, b);
}

using namespace std;

typedef long long ll;
typedef long double ld;

int N;
char str[60];

int rec(int pos) {
    if(pos == N - 1) return 0;
    if(str[pos] != str[pos+1]) {
        return rec(pos + 1);
    } else {
        return 1 + rec(pos + 1);
    }
}

int main(void) {
    scanf("%d%s", &N, str);
    printf("%d\n", rec(0));
    return 0;
}

