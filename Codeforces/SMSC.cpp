template<typename T> T gcd(T a, T b) {
    if(!b) return a;
    return gcd(b, a % b);
}
template<typename T> T lcm(T a, T b) {
    return a * b / gcd(a, b);
}

using namespace std;

typedef long long ll;
typedef long double ld;

int N, buff = 0, t, c, last = 0, last_time = 0;

int main(void) {
    int i, j;
    scanf("%d", &N);

    for (i = 0; i < N; i++) {
        scanf("%d%d", &t, &c);
        buff -= (t - last);
        buff = max(0, buff);
        buff += c;
        last_time = max(last_time, buff);
        last = t;
    }

    printf("%d %d\n", last + buff, last_time);

    return 0;
}
