int A, B, C, H, L;

bool ok(int AA, int BB) {
    return (AA <= H && BB <= L) || (AA <= L && BB <= H);
}

int main(void) {
    scanf("%d%d%d%d%d", &A, &B, &C, &H, &L);

    printf("%c\n", (ok(A, B) || ok(A, C) || ok(B, C)) ? 'S' : 'N');
    return 0;
}
